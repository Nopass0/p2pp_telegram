function formatDuration(duration: number) {
  const hours = Math.floor(duration / 3600);
  const minutes = Math.floor((duration % 3600) / 60);
  const seconds = duration % 60;
  
  return `${hours} —á ${minutes} –º–∏–Ω ${seconds} —Å–µ–∫`;
}
